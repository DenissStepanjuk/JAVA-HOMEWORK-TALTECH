package oop2;

/**
 *
 * @author jelena
 */
public abstract class Software {
    
    private String name;

    public Software(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return   "name=" + name ;
    }
    
    public abstract boolean isTimeLimit();
   
    
}
